    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        int indexNo = getLastId();
        String name = name_text.getText();
        String email = email_text.getText();
        String contactNo = contactNo_text.getText();

        MSupplier supplier = new MSupplier(indexNo, name, contactNo, email);

        try {
            boolean addSupplier = SupplierDao.getInstance().addSupplier(supplier);
            if (addSupplier) {
                getAllSupplier();
                doSelectSupplier();
            }
        } catch (SQLException ex) {
            Logger.getLogger(SearchSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

