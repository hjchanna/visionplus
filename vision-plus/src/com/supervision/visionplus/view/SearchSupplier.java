    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String name = name_text.getText();
        String contactNo = contactNo_text.getText();
        String email = email_text.getText();

        MSupplier supplier = new MSupplier();
        supplier.setName(name);
        supplier.setEmail(email);
        supplier.setContactNo(contactNo);

        try {
            ArrayList<MSupplier> suppliers = SupplierDao.getInstance().searchSupplier(supplier);
            addDataToTable(suppliers);
        } catch (SQLException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

