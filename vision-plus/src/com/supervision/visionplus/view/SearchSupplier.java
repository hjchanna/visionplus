    private void getAllSupplier() {
        try {
            ArrayList<MSupplier> supplier = SupplierDao.getInstance().getAllSupplier();
            addDataToTable(supplier);
        } catch (SQLException ex) {
            Logger.getLogger(SearchSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void addDataToTable(ArrayList<MSupplier> suppliers) {
        model.setRowCount(0);
        for (MSupplier supplier : suppliers) {
            Object[] rd = {supplier.getIndexNo(), supplier.getName(), supplier.getEmail(), supplier.getContactNo()};
            model.addRow(rd);
        }

    }

    private void doSelectSupplier() {
        if (supplierTable.getRowCount() == 1) {
            findSelectedCustomer(0);
        } else if (supplierTable.getSelectedRowCount() == 1) {
            findSelectedCustomer(supplierTable.getSelectedRow());

        } else {
            JOptionPane.showMessageDialog(this, "Select a Supplier..");
        }
    }

    private void findSelectedCustomer(int value) {
        int indexNo = Integer.parseInt(model.getValueAt(value, 0).toString());
        String name = model.getValueAt(value, 1).toString();
        String email = model.getValueAt(value, 2).toString();
        String contactNo = model.getValueAt(value, 3).toString();

        MSupplier supplier = new MSupplier();
        supplier.setIndexNo(indexNo);
        supplier.setName(name);
        supplier.setEmail(email);
        supplier.setContactNo(contactNo);

        this.dispose();
        grn.setCustomer(supplier);
    }

    void setFrame(Grn grn) {
        this.grn = grn;
    }

    int getLastId() {
        int newId = IDGenerator.getNewId("m_supplier", "index_no");
        return newId;
    }

