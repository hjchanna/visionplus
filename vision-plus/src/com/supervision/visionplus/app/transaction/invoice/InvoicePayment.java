    private void setBankBranch() {
        try {
            cardBankCombo1.removeAllItems();
            chequeBankCombo.removeAllItems();
            ArrayList<MBankBranch> BankBranches = BankBranchDao.getInstance().getAllBankBranch();
            for (MBankBranch bankBranche : BankBranches) {
                cardBankCombo1.addItem(bankBranche.getCode() + " " + bankBranche.getBank() + " " + bankBranche.getBranch());
                chequeBankCombo.addItem(bankBranche.getCode() + " " + bankBranche.getBank() + " " + bankBranche.getBranch());
            }
        } catch (SQLException ex) {
            Logger.getLogger(InvoicePayment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
